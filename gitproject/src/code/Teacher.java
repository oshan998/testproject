package code;

public class Teacher {
	
	private int name;
	private int salary;
	public Teacher(int name, int salary) {
		super();
		this.name = name;
		this.salary = salary;
	}
	public int getName() {
		return name;
	}
	public void setName(int name) {
		this.name = name;
	}
	public int getSalary() {
		return salary;
	}
	public void setSalary(int salary) {
		this.salary = salary;
	}
	
	

}
